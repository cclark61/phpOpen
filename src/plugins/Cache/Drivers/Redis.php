namespace phpOpenFW\Cache\Drivers;

//**************************************************************************************
/**
 * Redis Class
 */
//**************************************************************************************
class Redis
{
	//**********************************************************************************
	// Class Members
	//**********************************************************************************
	protected static $port = 6379;

	//**********************************************************************************
	// Constructor Method
	//**********************************************************************************
    public function __construct($params)
    {
        parent::__construct($params);
        $this->cache_obj = new \Redis(); 
        $this->cache_obj->connect($this->server, $this->port); 
    }

	//**********************************************************************************
	// Get Multiple Method
	//**********************************************************************************
	public function getMulti(Array $keys, Array $args=[])
	{
        return $this->cache_obj->mGet($keys);
	}

}
