namespace phpOpenFW\Builders\SQL\Statements\Traits;

//**************************************************************************************
/**
 * SQL From Trait
 */
//**************************************************************************************
trait From
{
    //=========================================================================
	// Trait Memebers
    //=========================================================================
	protected $from = [];

    //=========================================================================
    //=========================================================================
	// From Clause Method
    //=========================================================================
    //=========================================================================
	public function From($from)
	{
        self::AddItemCSC($this->from, $from);
    	return $this;
	}

    //=========================================================================
    //=========================================================================
	// Raw From Clause Method
    //=========================================================================
    //=========================================================================
	public function FromRaw($from)
	{
        self::AddItem($this->from, $from);
    	return $this;
	}

    //##################################################################################
    //##################################################################################
    //##################################################################################
    // Protected / Internal Methods
    //##################################################################################
    //##################################################################################
    //##################################################################################

    //=========================================================================
    //=========================================================================
    // Format From Method
    //=========================================================================
    //=========================================================================
    protected function FormatFrom()
    {
        return self::FormatCSC('FROM', $this->from);
    }

}
