namespace phpOpenFW\Builders\SQL;

//**************************************************************************************
/**
 * SQL Insert Class
 */
//**************************************************************************************
class Insert extends Core
{

    //=========================================================================
    //=========================================================================
    // Constructor Method
    //=========================================================================
    //=========================================================================
    public function __construct()
    {
	    $this->sql_type = 'insert';
	}

    //=========================================================================
    //=========================================================================
    // Get Method
    //=========================================================================
    //=========================================================================
    public function GetSQL()
    {
		$strsql = 'INSERT INTO ' . implode(', ', $this->from) . ' (';
		$strsql .= implode(', ', $this->fields) . ') VALUES (';
		$strsql .= ')';
		//$where = 
		return $strsql;
	}

}
