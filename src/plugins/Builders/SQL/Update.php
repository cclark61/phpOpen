namespace phpOpenFW\Builders\SQL;

//**************************************************************************************
/**
 * SQL Update Class
 */
//**************************************************************************************
class Update extends Core
{
    //=========================================================================
    // Traits
    //=========================================================================
    use Traits\Where;

    //=========================================================================
	// Class Memebers
    //=========================================================================
    protected $sql_type = 'update';
    protected $table = false;

    //=========================================================================
    //=========================================================================
    // Get SQL Method
    //=========================================================================
    //=========================================================================
    public function GetSQL()
    {
		$strsql = "UPDATE {$this->table} SET ";

		//-------------------------------------------------------
        // Format Fields
		//-------------------------------------------------------


		//-------------------------------------------------------
        // Format Where
		//-------------------------------------------------------
		$strsql .= $this->FormatWhere() . "\n";

		//-------------------------------------------------------
		// Return SQL
		//-------------------------------------------------------
		return $strsql;
	}

}
