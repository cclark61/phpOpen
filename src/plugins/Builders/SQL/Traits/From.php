namespace phpOpenFW\Builders\SQL\Traits;

//**************************************************************************************
/**
 * SQL From Trait
 */
//**************************************************************************************
trait From
{
    //=========================================================================
	// Trait Memebers
    //=========================================================================
	protected $from = [];

    //=========================================================================
    //=========================================================================
	// From Clause Method
    //=========================================================================
    //=========================================================================
	public function From($from)
	{
    	return $this->AddItem($this->from, $from);
	}

    //##################################################################################
    //##################################################################################
    //##################################################################################
    // Protected / Internal Methods
    //##################################################################################
    //##################################################################################
    //##################################################################################

    //=========================================================================
    //=========================================================================
    // Format From Method
    //=========================================================================
    //=========================================================================
    protected function FormatFrom()
    {
        if ($this->from) {
    		return "FROM\n  " . implode(",\n  ", $this->from);
        }

        return false;
    }

}
