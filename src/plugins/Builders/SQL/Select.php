namespace phpOpenFW\Builders\SQL;

//**************************************************************************************
/**
 * SQL Select Class
 */
//**************************************************************************************
class Select extends Core
{

    //=========================================================================
    //=========================================================================
    // Constructor Method
    //=========================================================================
    //=========================================================================
    public function __construct()
    {
	    $this->sql_type = 'select';
	}

    //=========================================================================
    //=========================================================================
    // Get Method
    //=========================================================================
    //=========================================================================
    public function GetSQL()
    {
		$strsql = 'SELECT ' . implode(', ', $this->fields) . ' ';
		$strsql .= ' FROM ' . implode(', ', $this->from) . ' ';
		//$where = 
		return $strsql;
	}

}
