    }

    //=========================================================================
    //=========================================================================
    // Add Bind Parameter
    //=========================================================================
    //=========================================================================
    protected static function AddBindParam(Array &$params, $value, $type='i')
    {
        if (count($params) == 0) {
            $params[] = '';
        }
        $params[0] = $type;
        $params[] = $value;
        return '?';
    }
    
