namespace phpOpenFW\Builders\SQL;

//**************************************************************************************
/**
 * SQL Delete Class
 */
//**************************************************************************************
class Delete extends Core
{
    //=========================================================================
    // Traits
    //=========================================================================
    use Traits\Where;

    //=========================================================================
	// Class Memebers
    //=========================================================================
    protected $sql_type = 'delete';
    protected $table = false;

    //=========================================================================
    //=========================================================================
    // Get SQL Method
    //=========================================================================
    //=========================================================================
    public function GetSQL()
    {
		$where = $this->FormatWhere();
		if ($where) {
    		return "DELETE FROM {$this->table} {$where}";
        }

        return false;
	}

}
