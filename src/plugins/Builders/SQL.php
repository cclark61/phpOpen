namespace phpOpenFW\Builders;

//**************************************************************************************
/**
 * SQL Class
 */
//**************************************************************************************
class SQL
{

    //=========================================================================
    //=========================================================================
    // Select Method
    //=========================================================================
    //=========================================================================
    public static function Select($fields=false, $table=false, $where=false)
    {
		$obj = new SQL\Select();
		$obj->Fields($fields);
		$obj->From($table);
		$obj->Where($where);
		return $obj;
	}

    //=========================================================================
    //=========================================================================
    // Insert Method
    //=========================================================================
    //=========================================================================
    public static function Insert($fields=false, $table=false)
    {
		$obj = new SQL\Insert();
		$obj->Fields($fields);
		$obj->From($table);
		return $obj;
	}

    //=========================================================================
    //=========================================================================
    // Update Method
    //=========================================================================
    //=========================================================================
    public static function Update($fields=false, $table=false, $where=false)
    {
		$obj = new SQL\Update();
		$obj->Fields($fields);
		$obj->From($table);
		$obj->Where($where);
		return $obj;
	}

    //=========================================================================
    //=========================================================================
    // Delete Method
    //=========================================================================
    //=========================================================================
    public static function Delete($table=false, $where=false)
    {
		$obj = new SQL\Delete();
		$obj->From($table);
		$obj->Where($where);
		return $obj;
	}


}
