namespace phpOpenFW\Builders;

//**************************************************************************************
/**
 * SQL Class
 */
//**************************************************************************************
class SQL
{

    //=========================================================================
    //=========================================================================
    // Select Method
    //=========================================================================
    //=========================================================================
    public static function Select(Array $args=[])
    {
		$obj = new SQL\Select();
		$obj->Fields($fields);
		$obj->From($table);
		$obj->Where($where);
		return $obj;
	}

    //=========================================================================
    //=========================================================================
    // Insert Method
    //=========================================================================
    //=========================================================================
    public static function Insert(Array $args=[])
    {
		$obj = new SQL\Insert();
		$obj->Into($table);
		$obj->Fields($fields);
		return $obj;
	}

    //=========================================================================
    //=========================================================================
    // Update Method
    //=========================================================================
    //=========================================================================
    public static function Update(Array $args=[])
    {
		$obj = new SQL\Update();
		$obj->Fields($fields);
		$obj->From($table);
		$obj->Where($where);
		return $obj;
	}

    //=========================================================================
    //=========================================================================
    // Delete Method
    //=========================================================================
    //=========================================================================
    public static function Delete(Array $args=[])
    {
		$obj = new SQL\Delete();
		$obj->From($table);
		$obj->Where($where);
		return $obj;
	}

}
